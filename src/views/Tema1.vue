      .h2 1.1 Política contable

    p Dentro del manual de políticas contables establecido por la empresa es de especial importancia el capítulo correspondiente a los inventarios, pues a través de este se establecen los lineamientos a seguir para la determinación del sistema y método de valuación de los inventarios y su correspondiente tratamiento en las diferentes operaciones.

    p De igual forma, la política contable de inventarios debe establecer los gastos correlacionados en una venta, donde lo principal corresponde al costo de venta, pues permite determinar el resultado de la operación.

