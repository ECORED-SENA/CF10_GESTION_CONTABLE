    p.mb-3 El reconocimiento de los inventarios consiste en el registro contable de los mismos para ser incorporado en los estados financieros.

    p.mb-3 De acuerdo con la sección 13 de NIIF para Pymes (2009), cuando los inventarios se vendan, la entidad reconocerá el importe en libros de estos como un costo (gasto) en el período en el que se reconozcan los correspondientes ingresos de actividades ordinarias.

    p.mb-3 En el proceso de compra se debe reconocer el impuesto a las ventas descontable y practicar las correspondientes retenciones sobre los impuestos a la renta, a industria y comercio e incluso a las ventas; dependiendo de las obligaciones tributarias y características fiscales del comprador y el proveedor.

    p.mb-3 Por otro lado, en el proceso de ventas se debe reconocer el impuesto a las ventas generado, al igual que los correspondientes anticipos por retenciones de los impuestos a la renta, de industria y comercio e incluso a las ventas; dependiendo de las obligaciones tributarias y características fiscales del vendedor y el cliente.

