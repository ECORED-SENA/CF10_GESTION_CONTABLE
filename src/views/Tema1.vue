      .h2 1.3 Medición, reconocimiento con impuestos y retenciones.
    .row.justify-content-center.align-items-center.mb-4
      .col-12.col-lg-6
        p La medición de los inventarios consiste en determinar el valor por el cual serán reconocidos en los estados financieros. La NIC 2, hace referencia de las técnicas para la medición inicial y posterior del costo de los inventarios, tales como el método del costo estándar o el método de los minoristas, los cuales podrán ser utilizadas por conveniencia siempre que el resultado de aplicarlas se aproxime al costo.
        p Los costos estándares tendrán en cuenta los niveles normales de materias primas, suministros, mano de obra, eficiencia y utilización de la capacidad. Estos se revisarán de forma regular y, si es necesario, se cambiarán en función de las condiciones actuales. 

        p El método de los minoristas se utiliza a menudo, en el sector comercial al por menor para la medición de inventarios cuando hay un gran número de artículos que rotan velozmente, que tienen márgenes similares y para los cuales resulta impracticable usar otros métodos de cálculo de costos. Cuando se emplea este método, el costo de los inventarios se determinará deduciendo, del precio de venta del artículo en cuestión, un porcentaje apropiado de margen bruto. El porcentaje aplicado tendrá en cuenta la parte de los inventarios que se han marcado por debajo de su precio de venta original. A menudo se utiliza un porcentaje promedio para cada sección o departamento comercial.       

