      h1 Inventarios

    p.mb-5 Antes de hablar del registro de compras y ventas, es importante enfocarse en el tema de los inventarios para la venta, producción o prestación de servicios, toda vez que constituyen la fuente generadora de ingresos para la empresa y por tanto requieren de un tratamiento especial.
    
    #t_1_1.titulo-segundo
      .h2 1.1 Política contable 

    p.mb-3 Dentro del manual de políticas contables establecido por la empresa es de especial importancia el capítulo correspondiente a los inventarios, pues a través de este se establecen los lineamientos a seguir para la determinación del sistema y método de valuación de los inventarios y su correspondiente tratamiento en las diferentes operaciones.

    p.mb-3 De igual forma, la política contable de inventarios debe establecer los gastos correlacionados en una venta, donde lo principal corresponde al costo de venta, pues permite determinar el resultado de la operación.

    p.mb-3 Otro aspecto fundamental que debe incluirse en la política de inventarios es el establecimiento de los precios de venta, pues la sección 13 de NIIF para Pymes (2009), requiere que se haga una comparación entre el costo de adquisición y los egresos necesarios para ponerlo en condiciones de venta y el precio de venta neto estimado, para determinar los costos de venta del inventario.

    p.mb-3 Además de las compras, ventas de inventarios y las devoluciones que de estas se desprenden, también hacen parte de las políticas contables, los traslados entre bodegas y los procesos para el tratamiento de deterioros, y bajas de inventarios. 

